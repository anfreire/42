#ifndef ICHARACTER_HPP
# define ICHARACTER_HPP

#include "AMateria.hpp"

// Because this is an interface, it is not intended to be canonical. The subject expressly states this.
// "Write the concrete class Character which will implement the following interface:"
// Only the concrete class that would be initiated should receive any additions, not this class.

class ICharacter
{
	public:
		virtual ~ICharacter() {}
		virtual std::string const & getName() const = 0;
		virtual void equip(AMateria* m) = 0;
		virtual void unequip(int idx) = 0;
		virtual void use(int idx, ICharacter& target) = 0;
};

#endif